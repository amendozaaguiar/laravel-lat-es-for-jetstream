Este proyecto usa **amendozaaguiar/laraveles-spanish-for-jetstream** para traducciones en español latinoamericano de Laravel + Jetstream.

- Instalar traducciones: `php artisan laravellates-jetstream:install`
- Verificar traducciones: `php artisan laravellates-jetstream:check`
- Archivos PHP en `lang/es/` (auth, pagination, passwords, validation)
- Traducciones Jetstream en `lang/es.json` (216+ cadenas de texto)
- Activa el skill **laraveles-spanish-for-jetstream** para documentación completa.
